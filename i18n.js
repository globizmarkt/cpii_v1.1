/**
 * Aplica las traducciones gestionando seguridad HTML y placeholders.
 * v2.5.0: Añade soporte para data-i18n-placeholder (inputs y selects)
 */
function applyTranslations(lang) {
    const dict = translations[lang] || translations["pt"];

    // 1. Traducir textos e innerHTML
    // 1. Traducir elementos con data-i18n — Parche de Seguridad XSS (R0)
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (dict[key] !== undefined) {
            if (el.dataset.i18nHtml === "true") {
                // Renderizado HTML solo bajo atributo explícito (Riesgo controlado)
                el.innerHTML = dict[key];
            } else if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = dict[key];
            } else {
                // Inyección segura por defecto (texto plano)
                el.textContent = dict[key];
            }
        }
    });

    // --- Extensión a Metadatos Head (Regla R4 Estricta) ---
    if (dict['metadata.title']) {
        document.title = dict['metadata.title'];
    }
    const metaDescription = document.querySelector('meta[name="description"]');
    if (metaDescription && dict['metadata.description']) {
        metaDescription.setAttribute('content', dict['metadata.description']);
    }

    // 2. Traducir placeholders — inputs y primer option vacío de selects
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (dict[key] !== undefined) {
            el.setAttribute("placeholder", dict[key]);
            // Si es <select>, actualizar también el texto del primer option vacío
            if (el.tagName.toLowerCase() === "select" &&
                el.options.length > 0 &&
                el.options[0].value === "") {
                el.options[0].text = dict[key];
            }
        }
    });

    document.documentElement.lang = lang;
}

/**
 * Cambia el idioma, lo persiste en localStorage y aplica las traducciones.
 * Dispara un evento personalizado para que otros componentes puedan reaccionar.
 */
function setLanguage(lang) {
    localStorage.setItem("cpii_locale", lang);
    applyTranslations(lang);
    document.dispatchEvent(new CustomEvent('cpii:lang:change', {
        detail: { lang, persistent: true }
    }));
}

// ══════════════════════════════════════════════════════════════ INICIALIZACIÓN ══
document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("cpii_locale") || "pt";

    const selector = document.getElementById("lang-selector");
    if (selector) {
        selector.value = savedLang;
        selector.addEventListener("change", (e) => {
            setLanguage(e.target.value);
        });
    }

    applyTranslations(savedLang);

    window.addEventListener('storage', (e) => {
        if (e.key === 'cpii_locale' && e.newValue) {
            applyTranslations(e.newValue);
            if (selector) selector.value = e.newValue;
        }
    });
});